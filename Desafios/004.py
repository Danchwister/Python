# Faça um Programa que peça as 4 notas bimestrais e mostre a média.


print("Bem-vindo à calculadora de Médias escolas.")
nota1= int (input("Qual a nota do primeiro Bimestre? "))
nota2= int (input("Qual a nota do segundo Bimestre? "))
nota3= int (input("Qual a nota do terceiro Bimestre? "))
nota4= int (input("Qual a nota do quarto Bimestre? "))

media_final= (nota1 + nota2 + nota3 + nota4) /4
print ("Sua média final é ", media_final)


